print("Preprocessing Completed Successfully")

from torch_geometric.data import Data

x = torch.tensor(df_sample[features].values, dtype=torch.float)
y = torch.tensor(df_sample['isFraud'].values, dtype=torch.long)

print("Tensor Shapes:", x.shape, y.shape)

from sklearn.model_selection import train_test_split
import numpy as np
import torch

# Create indices
indices = np.arange(len(df_sample))

# Split
train_idx, test_idx = train_test_split(
    indices,
    test_size=0.3,
    random_state=42,
    stratify=df_sample['isFraud']  # maintains fraud ratio
)

