# Load dataset
file_path = "/content/sample_data/PS_20174392719_1491204439457_log.xlsx" # Corrected file path and extension
df = pd.read_excel(file_path, header=None, names=column_names, skiprows=1) # Use pd.read_excel with correct parameters

# Ensure numeric columns are correctly typed, coercing errors to NaN
df['amount'] = pd.to_numeric(df['amount'], errors='coerce')
df['isFraud'] = pd.to_numeric(df['isFraud'], errors='coerce').astype('Int64') # Use Int64 for nullable integer
df['step'] = pd.to_numeric(df['step'], errors='coerce').astype('Int64')
df['oldbalanceOrg'] = pd.to_numeric(df['oldbalanceOrg'], errors='coerce')
df['newbalanceOrig'] = pd.to_numeric(df['newbalanceOrig'], errors='coerce')
df['oldbalanceDest'] = pd.to_numeric(df['oldbalanceDest'], errors='coerce')
df['newbalanceDest'] = pd.to_numeric(df['newbalanceDest'], errors='coerce')

print("Original Dataset Shape:", df.shape)

# Sampling (creates df_sample)
fraud_df = df[df['isFraud'] == 1]
nonfraud_population_size = len(df[df['isFraud'] == 0])
# Sample all available non-fraudulent records as the population is small
nonfraud_df = df[df['isFraud'] == 0].sample(n=nonfraud_population_size, random_state=42)

